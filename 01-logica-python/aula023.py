# Operador lógico "not"

# Usado para inverter expressões
# not True = False
# not False = True

# o if só executa o bloco quando a condição é verdadeira (True).
logado = False
if not logado:
    print('Você precisa fazer login.')
# então quase se usa o "If not, o logado que é False, é executado com True."

print()

print(not True)  # False
print(not False)  # True